package com.brtracker.services.midlink.processing.command;

public enum CommanState {
	APPROVAL_PENDING("APPROVAL_PENDING"),
	PENDING_DELIVERY("PENDING_DELIVERY"),
	DELIVERED("DELIVERED"),
	VERIFIED("VERIFIED"),
	CANCELED("CANCELED"),
	FAILED("FAILED");
	
	private String state;
	
	private CommanState(String state) {
		this.state = state;
	}

	public String state() {
		return state;
	}

}
