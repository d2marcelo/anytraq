package com.brtracker.shared.utils.packet;

public class InvalidPacketDefException extends PacketReaderException {

	private static final long serialVersionUID = 1L;

	public InvalidPacketDefException() {
		super();
	}

	public InvalidPacketDefException(String message, Throwable cause) {
		super(message, cause);
	}

	public InvalidPacketDefException(String message) {
		super(message);
	}

	public InvalidPacketDefException(Throwable cause) {
		super(cause);
	}

	public InvalidPacketDefException(String message, PacketReaderContext context) {
		super(message, context);
	}

	public InvalidPacketDefException(String message, Throwable e,
			PacketReaderContext context) {
		super(message, e, context);
	}

	public InvalidPacketDefException(Throwable e, PacketReaderContext context) {
		super(e, context);
	}

}
